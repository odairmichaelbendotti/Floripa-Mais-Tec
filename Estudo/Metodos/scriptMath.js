//Valor de pi
let number = Math.PI
console.log(number)

//Arredondar número. Decimal acima de 5 aredonda para cima e menor que 5 para baixo.
number = Math.round(Math.PI)
console.log(number)

//Arredondar para baixo
number = Math.floor(Math.PI)
console.log(number)

//Arredondar para cima
number = Math.ceil(5.6)
console.log(number)

//Número absoluto (o positivo dele)
number = Math.abs(-Math.PI)
console.log(number)

//Valor mínimo entre um intervalo
number = Math.min(74, 100, 600, 20, 1, 3)
console.log(number)

//Valor máximo entre um intervalo
number = Math.max(74, 100, 600, 20, 1, 3)
console.log(number)

//Retorna um númeor aleatório entre 0 e 1
number = Math.random();
console.log(number)

//Número aleatório de 0 a 100
number = Math.floor(Math.random() * 100);
console.log(number)

